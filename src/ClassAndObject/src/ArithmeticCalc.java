public class ArithmeticCalc {
    int a;
    int b;
    int add;
    int sub;
    int multi;
    int div;

    int OpetationAdd(int a,int b){
        add = a+b;
        return add;
    }
    int OperationSub(int a,int b){
        sub = a-b;
        return sub;
    }
    int OperationMulti(int a,int b){
        multi=a*b;
        return multi;
    }
    int OperationDiv(int a,int b){
        div=a/b;
        return div;
    }

}
